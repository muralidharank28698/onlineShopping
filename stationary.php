        <h2>History of stationery</h2>
        <p>
            Originally, the term 'stationery' referred to all products sold by a stationer, whose name indicated that his 
            book shop was on a fixed spot. This was usually somewhere near a university, and permanent, while medieval trading
            was mainly carried on by itinerant peddlers (including chapmen, who sold books) and others (such as farmers and craftsmen)
            at markets and fairs. It was a unique term used between the 13th and 15th centuries in the manuscript culture. Stationers' 
            shops were places where books were bound, copied, and published. These shops often loaned books to nearby-university students 
            for a fee. The books were loaned out in sections, allowing students to study or copy them, and the only way to get the next 
            part of the book was to return the previous section.[2] In some cases, stationers' shops became the preferred choice for scholars
            to find books, instead of university libraries due to stationers' shops' wider collection of books. [3]The Stationers' Company 
            formerly held a monopoly over the publishing industry in England and was responsible for copyright regulations
